# Bitwise Operators (AND,OR,XOR and NOT,Left Shift,Right Shift)

#Bitwise AND Operator
print(10 & 7)

#Bitwise OR Operator
print(8 | 4)

#Bitwise XOR Operator
print(10 ^ 7)

#Bitwise NOT Operator
print(	~10)
print(	~-10)

#Bitwise Left Shift operator
print(10 << 2)
#Bitwise Right Shift Operator
print(10 >> 2)

