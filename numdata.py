#int ,float , complex
#int:
a= 222222
b= 0
c= -11111

print(type(a))
print(type(b))
print(type(c))

#Float:
d= -9.5666 #decimal number
e= 0.00002
f= 2.4E000 #exponential number

print(type(d))
print(type(e))
print(type(f))

#Complex:
g = 2 + 4j
h = -(3 + 7j)
i = -4.1j
j = 6j

print(type(g))
print(type(h))
print(type(i))
print(type(j))